function select_greeting() {
  return greetings[Math.floor(Math.random() * greetings.length)];
}
function create_message() {
  return [
    select_greeting(),
    shuffle(messages)
      .slice(0, 2 + Math.floor(Math.random() * 3)) /* 2から4個返す */
      .join("\n")
  ].join("\n");
}
function create_hashtags() {
  return shuffle(hashtags)
    .slice(0, Math.floor(Math.random() * 5))
    .sort();
}
const display = shuffle(Array.prototype.concat(greetings, messages, hashtags));
function create_textarea(message: string, hashtags: string[]) {
  if (message && hashtags) {
    return [message, hashtags.join(" ")].join("\n");
  } else {
    return "aaaaa";
  }
}
